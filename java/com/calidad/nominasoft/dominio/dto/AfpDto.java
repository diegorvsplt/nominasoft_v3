package com.calidad.nominasoft.dominio.dto;

public class AfpDto {

  private Long idDto;
  private float descuentoDto;
  private String nombreDto;
  
  public Long getIdDto() {
    return idDto;
  }
  public void setIdDto(Long idDto) {
    this.idDto = idDto;
  }
  public float getDescuentoDto() {
    return descuentoDto;
  }
  public void setDescuentoDto(float descuentoDto) {
    this.descuentoDto = descuentoDto;
  }
  public String getNombreDto() {
    return nombreDto;
  }
  public void setNombreDto(String nombreDto) {
    this.nombreDto = nombreDto;
  }

  
}
